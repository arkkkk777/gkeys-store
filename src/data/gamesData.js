// Generate more games by duplicating with different IDs
const generateGames = (count, prefix = '') => {
  const result = [];
  for (let i = 0; i < count; i++) {
    const baseGame = baseGames[i % baseGames.length];
    result.push({
      ...baseGame,
      id: `${prefix}${i + 1}`,
    });
  }
  return result;
};

